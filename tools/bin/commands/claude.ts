function findGitRoot(startDir: string): string | undefined {
  let current = startDir;
  while (true) {
    if (fs.existsSync(path.join(current, '.git')))
      return current;
    const parent = path.dirname(current);
    if (parent === current)
      return undefined;
    current = parent;
  }
}

/** Check if the repo root is the public Datagrok repo (has js-api/ at root). */
function isPublicRepo(repoRoot: string): boolean {
  return fs.existsSync(path.join(repoRoot, 'js-api')) ||
    fs.existsSync(path.join(repoRoot, 'public', 'js-api'));
}

/**
 * Resolve the Datagrok version to use:
 * - Explicit --version flag takes priority
 * - Public repo: bleeding-edge (tracks development)
 * - Otherwise: latest
 */
function resolveDatagrokVersion(args: ClaudeArgs, repoRoot: string | undefined): string {
  if (args.version)
    return args.version;

  if (repoRoot && isPublicRepo(repoRoot)) {
    color.info('Public repo detected — using bleeding-edge for datagrok');
    return 'bleeding-edge';
  }

  return 'latest';
}

function getProjectDir(taskKey: string): string {
  return path.join(os.tmpdir(), `dg-pkg-${taskKey}`);
}

/** Find a free TCP port by binding to port 0 and reading the assigned port. */
function findFreePort(): Promise<number> {
  return new Promise((resolve, reject) => {
    const srv = net.createServer();
    srv.listen(0, '127.0.0.1', () => {
      const addr = srv.address();
      const port = typeof addr === 'object' && addr ? addr.port : 0;
      srv.close(() => resolve(port));
    });
    srv.on('error', reject);
  });
}

function writeProjectFiles(taskKey: string, args: ClaudeArgs, worktreeRoot: string, dgPort: number): string {
  const projectDir = getProjectDir(taskKey);
  if (!fs.existsSync(projectDir))
    fs.mkdirSync(projectDir, {recursive: true});

  // Write the embedded compose template
  fs.writeFileSync(path.join(projectDir, 'docker-compose.yaml'), COMPOSE_TEMPLATE);

  // Resolve Docker socket path
  const dockerSock = process.platform === 'win32'
    ? '//var/run/docker.sock'
    : '/var/run/docker.sock';

  // Generate .env — all paths use forward slashes for Docker compatibility
  const envLines: string[] = [
    `WORKTREE_PATH=${toDockerPath(worktreeRoot)}`,
    `DG_PORT=${dgPort}`,
    `TASK_KEY=${taskKey.toLowerCase()}`,
    `DOCKER_SOCK=${dockerSock}`,
    `DATAGROK_VERSION=${args.version || 'latest'}`,
    `DG_VERSION=${args.version || 'latest'}`,
    `GROK_CONNECT_VERSION=${args['grok-connect-version'] || 'latest'}`,
    `GROK_SPAWNER_VERSION=${args['grok-spawner-version'] || 'latest'}`,
    `JKG_VERSION=${args['jkg-version'] || 'latest'}`,
    `TOOLS_DEV_VERSION=${args['tools-dev-version'] || 'latest'}`,
    `FOLDER_NAME=${path.basename(worktreeRoot)}`,
  ];

  for (const env of ['ANTHROPIC_API_KEY', 'DG_PUBLIC_BRANCH', 'JIRA_URL', 'JIRA_USERNAME', 'JIRA_TOKEN', 'GITHUB_TOKEN'])
    if (process.env[env])
      envLines.push(`${env}=${process.env[env]}`);

  fs.writeFileSync(path.join(projectDir, '.env'), envLines.join('\n') + '\n');

  // Write host config compose override (mirrors deploy/fat_dev/dg-claude approach)
  const volumes: string[] = [];

  // Claude profile (~/.claude + ~/.claude.json)
  const claudeHome = findClaudeHome();
  if (claudeHome) {
    volumes.push(`      - "${toDockerPath(claudeHome)}:/home/node/.claude"`);
    const claudeState = path.join(path.dirname(claudeHome), '.claude.json');
    if (fs.existsSync(claudeState) && fs.statSync(claudeState).isFile())
      volumes.push(`      - "${toDockerPath(claudeState)}:/home/node/.claude.json"`);
    color.info(`Claude profile: ${claudeHome}`);
  }
  else
    color.warn('No Claude profile found. Set CLAUDE_HOME or run "claude" locally to log in.');

  const overridePath = path.join(projectDir, 'docker-compose.override.yaml');
  if (volumes.length > 0) {
    const override = [
      'services:',
      '  tools-dev:',
      '    volumes:',
      ...volumes,
    ].join('\n') + '\n';
    fs.writeFileSync(overridePath, override);
  }
  else if (fs.existsSync(overridePath))
    fs.unlinkSync(overridePath);

  return projectDir;
}

function runCompose(projectDir: string, taskKey: string, args: ClaudeArgs, ...composeArgs: string[]): number {
  // Use --project-directory so compose picks up .env automatically
  const cmdArgs = ['compose', '--project-directory', projectDir, '-p', `dg-pkg-${taskKey.toLowerCase()}`];
  // Include host config override if present (Claude profile, grok config)
  const overrideFile = path.join(projectDir, 'docker-compose.override.yaml');
  if (fs.existsSync(overrideFile))
    cmdArgs.push('-f', path.join(projectDir, 'docker-compose.yaml'), '-f', overrideFile);
  if (args.profile)
    cmdArgs.push('--profile', args.profile);
  cmdArgs.push(...composeArgs);

  const result = spawnSync('docker', cmdArgs, {
    stdio: 'inherit',
  });
  return result.status ?? 1;
}

/** Check if Datagrok responds with 200 at /api/info/server. */
function httpProbe(port: number): Promise<boolean> {
  return new Promise((resolve) => {
    const req = http.get(`http://localhost:${port}/api/info/server`, {timeout: 5000}, (res) => {
      res.resume();
      resolve(res.statusCode === 200);
    });
    req.on('error', () => resolve(false));
    req.on('timeout', () => { req.destroy(); resolve(false); });
  });
}

async function waitForDatagrok(port: number, timeoutMs: number = 180000): Promise<boolean> {
  const url = `http://localhost:${port}`;
  const start = Date.now();
  color.info(`Waiting for Datagrok at ${url}...`);

  while (Date.now() - start < timeoutMs) {
    if (await httpProbe(port)) {
      color.info('Datagrok is ready.');
      return true;
    }
    await new Promise((resolve) => setTimeout(resolve, 3000));
  }

  color.error(`Datagrok did not become healthy within ${timeoutMs / 1000}s`);
  return false;
}

function destroyProject(taskKey: string, args: ClaudeArgs, repoRoot?: string): boolean {
  const projectDir = getProjectDir(taskKey);
  if (!fs.existsSync(path.join(projectDir, 'docker-compose.yaml'))) {
    color.error(`No compose project found for "${taskKey}" in ${projectDir}`);
    return false;
  }
  color.info(`Destroying "${taskKey}"...`);
  runCompose(projectDir, taskKey, args, 'down', '-v');

  // Remove worktree
  const worktreePath = path.join(os.homedir(), 'pkg-worktrees', taskKey);
  if (fs.existsSync(worktreePath) && repoRoot) {
    spawnSync('git', ['worktree', 'remove', worktreePath, '--force'], {cwd: repoRoot, stdio: 'inherit'});
    spawnSync('git', ['branch', '-D', taskKey], {cwd: repoRoot, stdio: 'inherit'});
  }

  // Clean up project dir
  fs.rmSync(projectDir, {recursive: true, force: true});
  color.info(`Destroyed "${taskKey}".`);
  return true;
}

/** List all project keys that have a compose file in the temp dir. */
function listProjects(): string[] {
  const prefix = 'dg-pkg-';
  const tmpDir = os.tmpdir();
  const keys: string[] = [];
  for (const entry of fs.readdirSync(tmpDir)) {
    if (entry.startsWith(prefix) && fs.existsSync(path.join(tmpDir, entry, 'docker-compose.yaml')))
      keys.push(entry.slice(prefix.length));
  }
  return keys;
}

export async function claude(args: ClaudeArgs): Promise<boolean> {
  const subcommand = args._[1];
  const taskKey = subcommand === 'destroy' ? args._[2] : subcommand;

  // Handle destroy: bring down containers, remove worktree, clean up
  if (subcommand === 'destroy') {
    if (!taskKey) {
      color.error('Usage: grok claude destroy <project>');
      return false;
    }
    const repoRoot = findGitRoot(process.cwd());
    return destroyProject(taskKey, args, repoRoot);
  }

  // Handle destroy-all: destroy all known projects
  if (subcommand === 'destroy-all') {
    const projects = listProjects();
    if (projects.length === 0) {
      color.info('No projects found.');
      return true;
    }
    const repoRoot = findGitRoot(process.cwd());
    for (const key of projects)
      destroyProject(key, args, repoRoot);
    return true;
  }

  // Require project name
  if (!taskKey)
    return false;

  // Reject master/main as task key
  if (taskKey === 'master' || taskKey === 'main') {
    color.error(`Cannot use '${taskKey}' as project name — always work on a dedicated branch.`);
    return false;
  }

  // Find repo root (optional — not required for --in-place or non-git directories)
  const repoRoot = findGitRoot(process.cwd());
  const inPlace = args['in-place'] || !repoRoot;

  if (!repoRoot && !args['in-place'])
    color.warn('Not inside a git repository — running in-place (no worktree).');

  // Resolve Datagrok version (only datagrok image uses this; others default to latest)
  if (!args.version) {
    args.version = resolveDatagrokVersion(args, repoRoot);
    color.info(`Datagrok version: ${args.version}`);
  }

  // When inside a public repo, tell the container which branch to clone (avoids slow version resolution)
  if (repoRoot && isPublicRepo(repoRoot) && !process.env.DG_PUBLIC_BRANCH) {
    const branchResult = spawnSync('git', ['rev-parse', '--abbrev-ref', 'HEAD'], {cwd: repoRoot});
    const branch = branchResult.stdout?.toString().trim();
    if (branch)
      process.env.DG_PUBLIC_BRANCH = branch;
  }

  // Determine workspace root: worktree or current directory
  let worktreeRoot: string;
  if (inPlace) {
    worktreeRoot = process.cwd();
    color.info(`Working in-place: ${worktreeRoot}`);
  }
  else {
    const worktreesDir = path.join(os.homedir(), 'pkg-worktrees');
    const worktreePath = path.join(worktreesDir, taskKey);
    if (!fs.existsSync(worktreePath)) {
      fs.mkdirSync(worktreesDir, {recursive: true});
      const result = spawnSync('git', ['worktree', 'add', worktreePath, '-b', taskKey], {
        cwd: repoRoot,
        stdio: 'inherit',
      });
      if (result.status !== 0) {
        color.error('Failed to create git worktree.');
        return false;
      }
      color.info(`Created worktree: ${worktreePath}`);
    }
    else
      color.info(`Worktree already exists at ${worktreePath}`);
    worktreeRoot = worktreePath;
  }

  // Warn about missing optional environment variables
  const optionalVars = ['JIRA_URL', 'JIRA_USERNAME', 'JIRA_TOKEN', 'GITHUB_TOKEN'];
  const missing = optionalVars.filter((v) => !process.env[v]);
  if (missing.length > 0)
    color.warn(`Not set: ${missing.join(', ')}`);

  // Find a free port for Datagrok (stable across container restarts)
  const dgPort = args.port || await findFreePort();
  color.info(`Datagrok UI will be at: http://localhost:${dgPort}`);

  // Write compose + .env to temp dir (no external file dependencies)
  const projectDir = writeProjectFiles(taskKey, args, worktreeRoot, dgPort);
  color.info(`Workspace: ${worktreeRoot}`);

  // Start containers
  color.info('Starting containers...');
  const upStatus = runCompose(projectDir, taskKey, args, 'up', '-d', '--wait');
  if (upStatus !== 0) {
    color.error('Failed to start containers.');
    return false;
  }

  // Determine Claude working directory based on repo type
  // Only trust public-repo markers when we are inside a real git repo — packages can have
  // public/js-api via node_modules symlinks, which would cause a false positive.
  const containerName = `dg-pkg-${taskKey.toLowerCase()}-tools-dev-1`;
  let claudeWorkDir: string;
  if (repoRoot && fs.existsSync(path.join(repoRoot, 'js-api')))
    claudeWorkDir = '/workspace/repo';
  else if (repoRoot && fs.existsSync(path.join(repoRoot, 'public', 'js-api')))
    claudeWorkDir = '/workspace/repo/public';
  else {
    // External repo: entrypoint clones public repo and creates symlink — wait for it
    const folderName = path.basename(worktreeRoot);
    claudeWorkDir = `/workspace/datagrok/packages/${folderName}`;
    color.info(`Waiting for workspace at ${claudeWorkDir} (streaming container logs)...`);

    // Stream container logs so the user can see clone progress
    const logsProc = spawn('docker', ['logs', '-f', containerName], {stdio: 'inherit'});

    let ready = false;
    for (let i = 0; i < 120; i++) {
      const check = spawnSync('docker', ['exec', containerName, 'test', '-d', claudeWorkDir]);
      if (check.status === 0) { ready = true; break; }
      await new Promise((r) => setTimeout(r, 5000));
    }

    // Stop streaming logs
    logsProc.kill();

    if (!ready) {
      color.warn(`${claudeWorkDir} not available after 600s — falling back to /workspace/repo`);
      claudeWorkDir = '/workspace/repo';
    }
  }

  const claudeArgs = ['--dangerously-skip-permissions'];
  if (args.prompt)
    claudeArgs.push('-p', args.prompt);

  color.info(`Launching Claude Code in container ${containerName}...`);
  spawnSync('docker', ['exec', '-it', '-w', claudeWorkDir, containerName, 'claude', ...claudeArgs], {
    stdio: 'inherit',
  });

  // On exit, optionally stop containers
  if (!args.keep) {
    color.info('Stopping containers...');
    runCompose(projectDir, taskKey, args, 'down');
    color.info('Containers stopped.');
  }
  else
    color.info(`Containers left running (--keep). Use "grok claude destroy ${taskKey}" to tear down.`);

  return true;
}
