interface ClaudeArgs {
  _: string[];
  version?: string;
  profile?: string;
  keep?: boolean;
  port?: number;
  prompt?: string;
  'in-place'?: boolean;
  'grok-connect-version'?: string;
  'grok-spawner-version'?: string;
  'jkg-version'?: string;
  'tools-dev-version'?: string;
}

/** Convert a Windows path to Docker-compatible forward-slash format. */
function toDockerPath(p: string): string {
  return p.replace(/\\/g, '/');
}

/** Ensure a directory exists on the host before bind-mounting it. */
function ensureDir(dir: string): void {
  if (!fs.existsSync(dir))
    fs.mkdirSync(dir, {recursive: true});
}

/**
 * Locate the host Claude config directory by searching multiple candidate paths.
 * Mirrors the search logic from deploy/fat_dev/dg-claude.
 * Returns the path to the .claude directory, or undefined if not found.
 */
function findClaudeHome(): string | undefined {
  const candidates: string[] = [];

  // 1. Explicit CLAUDE_HOME env var
  if (process.env.CLAUDE_HOME)
    candidates.push(process.env.CLAUDE_HOME);

  // 2. OS home directory
  const home = os.homedir();
  candidates.push(path.join(home, '.claude'));

  // 3. USERPROFILE (Windows — may differ from homedir in some setups)
  if (process.env.USERPROFILE)
    candidates.push(path.join(process.env.USERPROFILE, '.claude'));

  // 4. APPDATA fallback (Windows: %APPDATA%/../.claude)
  if (process.env.APPDATA)
    candidates.push(path.join(process.env.APPDATA, '..', '.claude'));

  for (const candidate of candidates) {
    if (!candidate) continue;
    const resolved = path.resolve(candidate);
    if (fs.existsSync(path.join(resolved, '.credentials.json')))
      return resolved;
  }

  // Fall back to the first existing .claude directory even without .credentials.json
  for (const candidate of candidates) {
    if (!candidate) continue;
    const resolved = path.resolve(candidate);
    if (fs.existsSync(resolved) && fs.statSync(resolved).isDirectory())
      return resolved;
  }

  return undefined;
}

