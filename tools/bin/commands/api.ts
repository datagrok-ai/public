import fs from 'fs';
import path from 'path';
import walk from 'ignore-walk';
import Ajv from 'ajv';
import * as utils from '../utils/utils';
import * as color from '../utils/color-utils';

const annotationForApiFile = `/**\nThis file is auto-generated by the grok api command.\nIf you notice any changes, please push them to the repository.\nDo not edit this file manually.\n*/\n`;
const annotationForDbFile = `/**\nThis file is auto-generated by the grok api command from databases/<schema>/schema.json manifests.\nIf you notice any changes, please push them to the repository.\nDo not edit this file manually.\n*/\n`;

const sep = '\n';

const packageFuncDirs = ['package.ts', 'package.g.ts'];
const apiFile = 'package-api.ts';
const dbFile = 'db.ts';
const domainSchemaPath = path.join(path.dirname(path.dirname(__dirname)), 'domain-schema.schema.json');

const domainSystemColumns: [string, string][] = [
  ['id', 'string'], ['version', 'number'], ['created_on', 'string'],
  ['updated_on', 'string'], ['author_id', 'string'],
];

const domainTypeMap: {[type: string]: string} = {
  string: 'string', int: 'number', float: 'number', bool: 'boolean', datetime: 'string',
  string_list: 'string[]', ref: 'string', user: 'string', group: 'string',
};

function normEol(s: string): string {
  return s.replace(/\r\n/g, '\n');
}

const curDir = process.cwd();
const srcDir = path.join(curDir, 'src');
const funcFilePath = path.join(fs.existsSync(srcDir) ? srcDir : curDir, apiFile);
const packagePath = path.join(curDir, 'package.json');
const names = new Set<string>();

let _package: any = {};

function generateQueryWrappers(): void {
  const queriesDir = path.join(curDir, 'queries');
  if (!fs.existsSync(queriesDir))
    return;

  const files = walk.sync({
    path: './queries',
    ignoreFiles: ['.npmignore', '.gitignore'],
  });

  const wrappers = [];
  for (const file of files) {
    if (!file.endsWith(utils.queryExtension)) continue;
    const filepath = path.join(queriesDir, file);
    const script = fs.readFileSync(filepath, 'utf8');
    if (!script) continue;

    const queries = script.split(/--\s*end/).map((q) => q.trim()).filter((q) => q.length > 0);
    for (const q of queries) {
      const name = utils.getScriptName(q, utils.commentMap[utils.queryExtension]);
      if (!name) continue;

      checkNameColision(name);

      const tb = new utils.TemplateBuilder(utils.queryWrapperTemplate)
        .replace('FUNC_NAME', name)
        .replace('FUNC_NAME_LOWERCASE', name)
        .replace('PACKAGE_NAMESPACE', _package?.friendlyName ?? '');

      const description = utils.getScriptDescription(q, utils.commentMap[utils.queryExtension]);
      const inputs = utils.getScriptInputs(q, utils.commentMap[utils.queryExtension]);
      const outputType = utils.getScriptOutputType(q, utils.commentMap[utils.queryExtension]);
      const resolvedOutputType = outputType === 'void' ? utils.dgToTsTypeMap['dataframe'] : outputType;
      tb.replace('PARAMS_OBJECT', inputs)
        .replace('TYPED_PARAMS', inputs)
        .replace('FUNC_JSDOC', {description, inputs})
        .replace('OUTPUT_TYPE', resolvedOutputType);
      wrappers.push(tb.build(1));
    }
  }

  saveWrappersToFile('queries', wrappers);
}

function generateScriptWrappers(): void {
  const scriptsDir = path.join(curDir, 'scripts');
  const pythonDir = fs.existsSync(srcDir) ? path.join(srcDir, 'python') : path.join(curDir, 'python');
  if (!fs.existsSync(scriptsDir))
    return;
  const wrappers = [];

  for (let dir of [scriptsDir, pythonDir]) {
    if (!fs.existsSync(dir))
      continue;
    const files = walk.sync({
      path: dir,
      ignoreFiles: ['.npmignore', '.gitignore'],
    });
    for (const file of files) {
      let extension: string;
      if (!utils.scriptExtensions.some((ext) => (extension = ext, file.endsWith(ext)))) continue;

      const filepath = path.join(dir, file);
      const script = fs.readFileSync(filepath, 'utf8');
      if (!script) continue;

      const name = utils.getScriptName(script, utils.commentMap[extension!]);
      if (!name) continue;
      const description = utils.getScriptDescription(script);

      checkNameColision(name)

      const tb = new utils.TemplateBuilder(utils.scriptWrapperTemplate)
        .replace('FUNC_NAME', name)
        .replace('FUNC_NAME_LOWERCASE', name)
        .replace('PACKAGE_NAMESPACE', _package?.friendlyName ?? '');

      const inputs = utils.getScriptInputs(script);
      const outputType = utils.getScriptOutputType(script);
      tb.replace('PARAMS_OBJECT', inputs)
        .replace('TYPED_PARAMS', inputs)
        .replace('FUNC_JSDOC', {description, inputs})
        .replace('OUTPUT_TYPE', outputType);
      wrappers.push(tb.build(1));
    }
  }

  saveWrappersToFile('scripts', wrappers);
}

function generateFunctionWrappers(): void {
  let filesToParse = packageFuncDirs.map((e) => path.join(curDir, 'src', e)).filter((e) => fs.existsSync(e));

  const annotaionRegex = /(?:\/\/[^\n]*\n)+export[^{]*/g;
  const nameRegex = /\s*export(?:\sasync)?\s*function\s*([^\s(]*)/;

  const wrappers = [];
  for (const file of filesToParse) {
    const fileData = fs.readFileSync(file, 'utf8');
    const annotations = fileData.matchAll(annotaionRegex);
    if (annotations === null)
      return;

    for (let annotation of annotations) {
      const name = (annotation[0].match(nameRegex) ?? [undefined, undefined])[1];
      const description = utils.getScriptDescription(annotation[0], utils.commentMap[utils.jsExtention]);
      if (!name)
        continue;

      const annotationInputs = utils.getScriptInputs(annotation[0], utils.commentMap[utils.jsExtention]);
      const annotationOutputDir = utils.getScriptOutputType(annotation[0], utils.commentMap[utils.jsExtention]);
      let outputType = '';

      outputType = annotationOutputDir ?? 'any';
      checkNameColision(name);

      const tb = new utils.TemplateBuilder(utils.scriptWrapperTemplate)
        .replace('FUNC_NAME', name)
        .replace('FUNC_NAME_LOWERCASE', name)
        .replace('PACKAGE_NAMESPACE', _package?.friendlyName ?? '')
        .replace('PARAMS_OBJECT', annotationInputs)
        .replace('FUNC_JSDOC', {description, inputs: annotationInputs})
        .replace('TYPED_PARAMS', annotationInputs)
        .replace('OUTPUT_TYPE', outputType);
      wrappers.push(tb.build(1));
    }
  }

  saveWrappersToFile('funcs', wrappers);
}

function saveWrappersToFile(namespaceName: string, wrappers: string[]) {
  if (wrappers.length === 0)
    return;
  if (!fs.existsSync(funcFilePath))
    createApiFile();

  const scriptApi = new utils.TemplateBuilder(utils.namespaceTemplate)
    .replace('PACKAGE_NAMESPACE', namespaceName)
    .replace('NAME', wrappers.join(sep.repeat(2)));
  fs.appendFileSync(funcFilePath, normEol(sep + scriptApi.build() + sep));
  color.log(`Successfully generated file ${apiFile}${sep}`, 'success');
}

function createApiFile() {
  fs.writeFileSync(funcFilePath, normEol(annotationForApiFile + utils.dgImports + sep), 'utf8');
}

function checkNameColision(name: string) {
  if (names.has(name))
    console.log('There is collision in name ' + name)
  names.add(name);
}

/** Generates `src/generated/db.ts` with typed clients for the package's domain schemas
 * (`databases/<schema>/schema.json` manifests). No-op for packages without manifests. */
export function generateDomainClients(packageDir: string = curDir): boolean {
  const databasesDir = path.join(packageDir, 'databases');
  if (!fs.existsSync(databasesDir))
    return true;

  const manifestPaths = fs.readdirSync(databasesDir)
    .map((dir) => path.join(databasesDir, dir, 'schema.json'))
    .filter((p) => fs.existsSync(p));
  if (manifestPaths.length === 0)
    return true;

  const validateManifest = new Ajv().compile(JSON.parse(fs.readFileSync(domainSchemaPath, 'utf8')));

  const parts: string[] = [];
  const emittedTypes = new Set<string>();
  for (const manifestPath of manifestPaths) {
    const relPath = path.relative(packageDir, manifestPath);
    let manifest: any;
    try {
      manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
    } catch (x: any) {
      color.error(`Failed to parse ${relPath}: ${x.message}`);
      return false;
    }
    if (!validateManifest(manifest)) {
      for (const err of validateManifest.errors ?? [])
        color.error(`${relPath}: ${err.instancePath || '/'} ${err.message}`);
      return false;
    }
    const code = generateDomainSchemaCode(manifest, relPath, emittedTypes);
    if (code == null)
      return false;
    parts.push(code);
  }

  const genDir = path.join(packageDir, 'src', 'generated');
  fs.mkdirSync(genDir, {recursive: true});
  const content = annotationForDbFile + utils.dgImports + sep + parts.join(sep);
  fs.writeFileSync(path.join(genDir, dbFile), normEol(content).replace(/\n/g, '\r\n'), 'utf8');
  color.log(`Successfully generated file src/generated/${dbFile}${sep}`, 'success');
  return true;
}

/** Emits row/insert interfaces, column-name unions, and the `<schema>Db` typed clients
 * for one manifest. Returns null on a semantic error (reported to the console). */
function generateDomainSchemaCode(manifest: any, manifestPath: string, emittedTypes: Set<string>): string | null {
  const decls: string[] = [];
  const clients: string[] = [];
  const systemColumnNames = new Set(domainSystemColumns.map(([name]) => name));
  for (const tableName of Object.keys(manifest.tables)) {
    const table = manifest.tables[tableName];
    const typeName = utils.snakeToCamelCase(tableName);
    if (emittedTypes.has(typeName)) {
      color.error(`${manifestPath}: table '${tableName}' emits interface '${typeName}Row' ` +
        `already generated for another table (table names must be unique across the package's manifests)`);
      return null;
    }
    emittedTypes.add(typeName);
    const columns: {name: string, tsType: string, required: boolean}[] = [];
    const columnNames = new Set(systemColumnNames);
    const addColumn = (name: string, column: any): boolean => {
      if (columnNames.has(name)) {
        color.error(systemColumnNames.has(name)
          ? `${manifestPath}: table '${tableName}' column '${name}' collides with a generated system column`
          : `${manifestPath}: table '${tableName}' declares duplicate column '${name}'`);
        return false;
      }
      columnNames.add(name);
      columns.push({name: name, tsType: domainTypeMap[column.type], required: column.required === true});
      return true;
    };
    for (const columnName of Object.keys(table.columns))
      if (!addColumn(columnName, table.columns[columnName]))
        return null;
    for (const schemaName of table.schemas ?? []) {
      const props = manifest.propertySchemas?.[schemaName];
      if (props == null) {
        color.error(`${manifestPath}: table '${tableName}' references unknown property schema '${schemaName}'`);
        return null;
      }
      for (const propName of Object.keys(props))
        if (!addColumn(propName, props[propName]))
          return null;
    }

    const rowLines = [`/** Row of \`${manifest.name}.${tableName}\`. */`, `export interface ${typeName}Row {`];
    for (const [name, tsType] of domainSystemColumns)
      rowLines.push(`  ${name}: ${tsType};`);
    for (const c of columns)
      rowLines.push(`  ${c.name}${c.required ? '' : '?'}: ${c.tsType};`);
    rowLines.push('}');
    decls.push(rowLines.join(sep));

    const insertLines = [`/** Insert payload for \`${manifest.name}.${tableName}\`. */`,
      `export interface ${typeName}Insert {`];
    for (const c of columns)
      insertLines.push(`  ${c.name}${c.required ? '' : '?'}: ${c.tsType};`);
    if (table.idempotency === true)
      insertLines.push('  idempotencyKey?: string;');
    insertLines.push('}');
    decls.push(insertLines.join(sep));

    decls.push(formatColumnUnion(`${typeName}Column`,
      [...domainSystemColumns.map(([name]) => name), ...columns.map((c) => c.name)]));

    const clientHead = `  ${utils.snakeToCamelCase(tableName, false)}: ` +
      `grok.dapi.domains.table('${manifest.name}.${tableName}') as`;
    const clientType = `DG.DomainTableClient<${typeName}Row, ${typeName}Insert>,`;
    clients.push(clientHead.length + clientType.length + 1 > 120 ?
      `${clientHead}${sep}    ${clientType}` : `${clientHead} ${clientType}`);
  }

  decls.push([`/** Typed clients for the \`${manifest.name}\` domain schema tables. */`,
    `export const ${utils.snakeToCamelCase(manifest.name, false)}Db = {`, ...clients, '};'].join(sep));
  return decls.join(sep.repeat(2)) + sep;
}

/** Formats `export type <name> = 'a' | 'b' | ...;` wrapped to the 120-char line limit. */
function formatColumnUnion(name: string, values: string[]): string {
  const lines: string[] = [];
  let line = `export type ${name} = `;
  for (let i = 0; i < values.length; i++) {
    const token = `'${values[i]}'` + (i < values.length - 1 ? ' | ' : ';');
    if (line.length + token.length > 118) {
      lines.push(line.trimEnd());
      line = '  ';
    }
    line += token;
  }
  lines.push(line);
  return lines.join(sep);
}

export function api(args: { _: string[], verbose?: boolean, v?: boolean }): boolean {
  color.setVerbose(args.verbose || args.v || false);
  _package = JSON.parse(fs.readFileSync(packagePath, { encoding: 'utf-8' }));
  if (_package.friendlyName)
    _package.friendlyName = _package.friendlyName.replaceAll(' ', '')
  const nOptions = Object.keys(args).length - 1 - (args.verbose ? 1 : 0) - (args.v ? 1 : 0);
  if (args['_'].length !== 1 || nOptions > 0) return false;
  if (!utils.isPackageDir(process.cwd())) {
    color.error('File `package.json` not found. Run the command from the package directory');
    return false;
  }
  createApiFile();
  generateScriptWrappers();
  generateQueryWrappers();
  generateFunctionWrappers();
  return generateDomainClients();
}
