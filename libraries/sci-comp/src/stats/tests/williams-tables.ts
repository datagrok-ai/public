/** Return the largest tabled df ≤ v (conservative). */
function pickDf(dfList: number[], v: number): number {
  const finite = dfList.filter((d) => d !== INF);
  const maxFinite = Math.max(...finite);
  if (v >= maxFinite)
    return dfList.includes(INF) ? INF : maxFinite;
  // Binary-search-style: largest finite ≤ v
  let chosen = finite[0];
  for (const d of finite) {
    if (d <= v) chosen = d;
    else break;
  }
  return chosen;
}

/**
 * Williams (1971) critical value for equal replication.
 *
 * @param k     Number of dose levels (1–10).
 * @param v     Error degrees of freedom.
 * @param alpha 0.05 or 0.01 (one-sided).
 */
export function lookup1971(k: number, v: number, alpha: number): number {
  const table = alpha === 0.05 ? WILLIAMS_1971_ALPHA_05 : WILLIAMS_1971_ALPHA_01;
  const col = table.kValues.indexOf(k);
  const df = pickDf(table.df, v);
  return table.values.get(df)![col];
}

/**
 * Williams (1972) critical value with extrapolation for w > 1.
 *
 * @param i     Dose level being tested (2, 3, 4, 5, 6, 8, or 10).
 * @param v     Error degrees of freedom.
 * @param alpha One-sided α: 0.050, 0.025, 0.010, or 0.005.
 * @param w     Replication ratio c/r (control/dose). Default 1.0.
 */
export function lookup1972(i: number, v: number, alpha: number, w = 1.0): number {
  const table = pickAlphaTable(alpha);
  const col = table.doseLevels.indexOf(i);
  const df = pickDf(table.df, v);
  const [tVal, beta] = table.values.get(df)![col];
  if (w <= 1.0) return tVal;
  const adjustment = 1e-2 * beta * (1.0 - 1.0 / Math.sqrt(w));
  return Math.round((tVal - adjustment) * 1000) / 1000;
}

function pickAlphaTable(alpha: number) {
  if (alpha === 0.050) return WILLIAMS_1972_ALPHA_050;
  if (alpha === 0.025) return WILLIAMS_1972_ALPHA_025;
  if (alpha === 0.010) return WILLIAMS_1972_ALPHA_010;
  if (alpha === 0.005) return WILLIAMS_1972_ALPHA_005;
  throw new Error(`lookup1972: unsupported alpha ${alpha}`);
}
